#include "BasicMath.h"

double BasicMath::Add(double value1, double value2)
{
	return value1 + value2;
}

double BasicMath::Subtract(double value1, double value2)
{
	return value1 - value2;
}

double BasicMath::Multiply(double value1, double value2)
{
	return value1 * value2;
}

double BasicMath::Divide(double value1, double value2)
{
	return value1 / value2;
}

double BasicMath::Average(double value1, double value2)
{
	return (value1 + value2) / 2;
}